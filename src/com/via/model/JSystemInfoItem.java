package com.via.model;

public class JSystemInfoItem {

	private String storageType;
	private String storageDescr;
	private String storageSize;
	private String storageUsed;
	private String processorLoad;
	private String storageIndex;
	private String processorIndex;
	
	public String getStorageType() {
		return storageType;
	}
	public void setStorageType(String storageType) {
		this.storageType = storageType;
	}
	public String getStorageDescr() {
		return storageDescr;
	}
	public void setStorageDescr(String storageDescr) {
		this.storageDescr = storageDescr;
	}
	public String getStorageSize() {
		return storageSize;
	}
	public void setStorageSize(String storageSize) {
		this.storageSize = storageSize;
	}
	public String getStorageUsed() {
		return storageUsed;
	}
	public void setStorageUsed(String storageUsed) {
		this.storageUsed = storageUsed;
	}
	public String getProcessorLoad() {
		return processorLoad;
	}
	public void setProcessorLoad(String processorLoad) {
		this.processorLoad = processorLoad;
	}
	public String getStorageIndex() {
		return storageIndex;
	}
	public void setStorageIndex(String storageIndex) {
		this.storageIndex = storageIndex;
	}
	public String getProcessorIndex() {
		return processorIndex;
	}
	public void setProcessorIndex(String processorIndex) {
		this.processorIndex = processorIndex;
	}
	

}
