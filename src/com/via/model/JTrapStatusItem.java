package com.via.model;

public class JTrapStatusItem {

	private int index;
	private String trapDestCommunity;
	private String trapDestStatus;
	private String trapDestVersion;
	private String trapDestUdpPort;
	private String trapIpAddress;

	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}
	public String getTrapDestCommunity() {
		return trapDestCommunity;
	}
	public void setTrapDestCommunity(String trapDestCommunity) {
		this.trapDestCommunity = trapDestCommunity;
	}
	public String getTrapDestStatus() {
		return trapDestStatus;
	}
	public void setTrapDestStatus(String trapDestStatus) {
		this.trapDestStatus = trapDestStatus;
	}
	public String getTrapDestVersion() {
		return trapDestVersion;
	}
	public void setTrapDestVersion(String trapDestVersion) {
		this.trapDestVersion = trapDestVersion;
	}
	public String getTrapDestUdpPort() {
		return trapDestUdpPort;
	}
	public void setTrapDestUdpPort(String trapDestUdpPort) {
		this.trapDestUdpPort = trapDestUdpPort;
	}
	public String getTrapIpAddress() {
		return trapIpAddress;
	}
	public void setTrapIpAddress(String trapIpAddress) {
		this.trapIpAddress = trapIpAddress;
	}


	
}
